#Funciones Integradas

'''
Sirven para hacer una conversión de datos entre sí.
'''

a = int("10") #Convertir una cadena a un valor numérico.
print(a)

b1 = float("10.8") #Convertir una cadena a un valor numerico.
print(b1)

c = str(15) #Convertir un numero a cadena con la función "Str".
print(c)

d = bin(20) #Convertir un numero a binario.
print(d)

e = hex(15) #Convertir un numero a hexagesimal
print(e)

f = int("0b1010", 2) #Convertir un binario a entero.
print(f)

g = int("0xa",16) #Convertir de hexagesimal a entero.
print(g)

h = abs(-8)#Mostrar el valor absoluto de un numero (Distancia a 0), pero siempre se muestra en positivo.
print(h)

i = round(5.6) #Función que redondea un numero.
print(i)

j = len("Felipe") #Función que permite contar cuantos caracteres tiene uan cadena.
print(j)
