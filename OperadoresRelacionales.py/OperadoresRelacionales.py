# Operadores Relacionales

'''
-Se utilizan para establecer una relaci√≥n entre 2 valores
-Compara estos valores entre si y esta comparacion produce un resultado que puede ser verdadero o falso
-tiene el mismo nivel de prioridad de evaluacion de izquierda a derecha
-Tiene menor prioridad que los operadores aritmeticos

Los operadores relacionales son:
< (Menor)
> (Mayor)
<= (Menor o igual que)
>= (Mayor o igual que)
!= (Diferente)
== (igual)
'''

a = 10
b = 20
c = 30

menor = a < b
print(menor)

mayor = a > b
print(mayor)

igualdad = a == b
print(igualdad)

diferencia = a != b
print(diferencia)

resultado = a + b == c
print(resultado)



