# Entrada de datos

nombre = input("Escriba su nombre: ") #Solo así se guardan los datos de tipo cadena con el parametro "Input".
print (f"Hola {nombre} ¿Cómo estás?")

numero = int(input("Ingrese un numero: ")) #Solo así se guardan los datos numéricos.
print(f"El numero ingresado es {numero}")

numero = float(input("Ingrese un numero decimal: ")) #Solo asi se guardan numeros con coma.
print(f"El numero ingresado es {numero}")
