# Operadores De Asignación

'''
Función principal: Nos permiten escribir de una forma más sencilla que nos ayuda a ahorrar código.
a = 0
Forma tradicional: a = a + 5.
Operador de Asignación: a+=5 #suma de asignación.
'''

a = 0
a += 5 #Suma de asignación.
a -= 2 #Resta de asignación.
a *= 3 #Multiplicación de asignación.
a /= 3 #División de asignación.
a **= 2 #Potencia de asignación en este caso: "a" elevado al cuadrado.
a %= 2 #Módulo de asignación, en este caso  para saber si es un numero par.

print(a)

